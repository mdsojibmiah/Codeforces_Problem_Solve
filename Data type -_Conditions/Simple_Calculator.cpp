#include <iostream>
using namespace std;
int main(){
	long long X,Y;
	cin >> X >> Y;
	cout << X << " " << "+" << " " << Y << " " << "=" << " " << X+Y << "\n";
	cout << X << " " << "*" << " " << Y << " " << "=" << " " << X*Y << "\n";
	cout << X << " " << "-" << " " << Y << " " << "=" << " " << X-Y << "\n";
	return 0;
}